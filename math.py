#Operações aritiméticas

#4 operações básicas
soma = 3 + 4
print(soma)
subtracao = 3 - 2
print(subtracao)
multiplicacao = 3 * 2
print(multiplicacao)
divisao = 10 / 2
print(divisao)

#Divisão inteira, sem resto
divInteira = 3//4
print(divInteira)

#Resto de divisão inteira
restoDiv = 3%4
print(restoDiv)

#Potenciação
potenciacao = 3**4
print(potenciacao)

#--------------------------------------------
#Arredontamento
x = round(2.7)
print(x)

#--------------------------------------------
#Notação científica
print(1.2e10)
